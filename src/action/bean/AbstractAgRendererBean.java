package bean;

import java.io.Serializable;


public abstract class AbstractAgRendererBean implements Serializable {
	
	protected Integer currentSortId;

	protected Boolean[] renderFlags;


	public Integer getCurrentSortId() {
		return currentSortId;
	}


	public void setCurrentSortId(Integer currentSortId) {
		this.currentSortId = currentSortId;
	}

	public abstract String toggleRendering(Integer sortId);
}
