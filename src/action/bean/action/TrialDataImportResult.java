package bean.action;

import entities.TrialData;

public class TrialDataImportResult {
	
	private TrialData trialData;
	
	private int importCnt;
	
	private int succCnt;

	public TrialData getTrialData() {
		return trialData;
	}

	public void setTrialData(TrialData trialData) {
		this.trialData = trialData;
	}

	public int getImportCnt() {
		return importCnt;
	}

	public void setImportCnt(int importCnt) {
		this.importCnt = importCnt;
	}

	public int getSuccCnt() {
		return succCnt;
	}

	public void setSuccCnt(int succCnt) {
		this.succCnt = succCnt;
	}
}
