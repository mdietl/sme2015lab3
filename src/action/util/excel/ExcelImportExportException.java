package util.excel;

public class ExcelImportExportException extends Exception {

	private static final long serialVersionUID = 1L;

	public ExcelImportExportException() {
	}

	public ExcelImportExportException(String message) {
		super(message);
	}

	public ExcelImportExportException(Throwable cause) {
		super(cause);
	}

	public ExcelImportExportException(String message, Throwable cause) {
		super(message, cause);
	}

}
