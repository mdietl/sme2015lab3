package util.xml;

public class XmlImportExportException extends Exception {

	private static final long serialVersionUID = 1L;

	public XmlImportExportException() {
	}

	public XmlImportExportException(String msg) {
		super(msg);
	}

	public XmlImportExportException(Throwable t) {
		super(t);
	}

	public XmlImportExportException(String msg, Throwable t) {
		super(msg, t);
	}

}
